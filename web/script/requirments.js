function getModuleRequirements(type) {
    return MODULE_REQUIREMENTS[type] || {
        title: 'Неизвестный модуль',
        data: {
            'Ошибка': ['Информация о требованиях отсутствует.']
        }
    };
}
function showRequirementsDialog(moduleType) {
    const module = getModuleRequirements(moduleType);

    const modal = document.createElement('div');
    modal.classList.add('requirements-dialog');

    const dialog = document.createElement('div');
    dialog.classList.add('requirements-window');

    const title = document.createElement('h3');
    title.textContent = 'Требования: ' + module.title;
    title.classList.add('requirements-title');

    const closeBtn = document.createElement('button');
    closeBtn.textContent = 'Закрыть';
    closeBtn.classList.add('requirements-close-btn');
    closeBtn.onclick = () => modal.remove();

    dialog.appendChild(closeBtn);
    dialog.appendChild(title);

    for (const [section, items] of Object.entries(module.data)) {
        const subtitle = document.createElement('p');
        subtitle.textContent = section;
        subtitle.classList.add('requirements-subtitle');
        dialog.appendChild(subtitle);

        const ul = document.createElement('ul');
        ul.classList.add('requirements-list');

        items.forEach(item => {
            const li = document.createElement('li');
            li.textContent = item;
            ul.appendChild(li);
        });

        dialog.appendChild(ul);
    }

    modal.appendChild(dialog);
    document.body.appendChild(modal);
}